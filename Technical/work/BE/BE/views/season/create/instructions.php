		<h1>Creating a Season</h1>
		<p>Before you continue, you will need:</p>
		<ul class="checklist">
			<li>A name for your season. This should easily identify the date range you are defining.</li>
			<li>The starting date of your season.</li>
			<li>The end date of your season.</li>
			<li>Registration opening date.</li>
			<li>Registration closing date.</li>
			<li>The amount of the deposit due upon registration.</li>
			<li>The amount of the total fees due upon registration.</li>
