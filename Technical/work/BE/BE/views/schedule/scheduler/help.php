<div id='help-extra' class='hidden'>

<h1>Help: Extra Games Menu</h1>
<ul>
<li>this screen is optional.  With it, you can create specific games that will be included in the schedule. </li>

<li>The schedule wizard will not move this game from the time and location  you have assigned it</li>
<li>these games will not count towards the division matches you created</li>
<li>these games WILL count towards all the minimum and maximum rules you specified</li>
<li>the dates and venues you pick here do not have to exist in any date list you created</li>
</ul>
