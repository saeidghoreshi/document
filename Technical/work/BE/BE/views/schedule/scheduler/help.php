<h1>Help: Fields Menu</h1>
<ul>
<li>if you delete a date list or timeslot list, you will lose everything it contains (dates, venues, etc)</li>

<li>For a simple schedule, it may be enough to just create one date list and one timeslot list</li>
<li>Give your lists useful names, such as 'all fridays in july'. </li>
<li>selected dates and venues may not be saved if you click on a menu item from that screen. please use the Save buttons at the bottom of each screen</li>
<li>If the "Review" section says "No records found.", return to "Select Times" and check the "# Dates" and "# Venues" columns.  If a timeslot has zero dates 
or zero venues, then this timeslot is not complete, and there are no selections to create.</li>
</ul>
