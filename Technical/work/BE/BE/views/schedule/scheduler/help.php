<div id='help-matches' class='hidden'>

<h1>Help: Matches Menu</h1>
<ul>
<li>First, click add match, which creates an empty row.</li>
<li>you cannot add matches while the filter is active.  to create more matches, swich the filter to "All"</li>
<li>To fill in the details of a match, click on the empty space in the column. for example, for divisions, a drop down will appear.</li>
<li>filling in a number for rounds or games will update the other based on the number of teams in those divisions</li>
<li>be careful, if you change the division of an existing match, the games and rounds numbers will be erased</li>
<li>each match must be assigned at leats one group of dates. do do this, click on the calendar picture in the row, which will allow you to 
	check and uncheck the groups of dates</li>
<li>Do not worry if you create too many empty rows, these will be ignored.</li>
</ul>
</div>
<div id='help-rules' class='hidden'>

<h1>Help: Rules Menu</h1>
<ul>
<li>Most of the rules displayed here are optional.  You can leave everything in their default settings to  create a schedule</li>
<li>Global: you have the option to set the number of games per team over this entire schedule.  </li>
<li>for all # fields, zero or blank means no maximum</li>
<li>if any rules conflict, the conflicts menu item will describe the problem.  This detects if you have entered some combination of min and max games required in different areas (		lists, divisions, globally , etc) that are impossible to satisfy.</li>
